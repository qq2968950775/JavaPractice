package java.lang;

public class String {
	// 静态代码块
	static {
		System.out.println("自定义的String类静态代码块");
	}

	public static void main(String[] args) {
		System.out.println("hello2");
	}
	/*
	 * 加main方法 错误: 在类 java.lang.String 中找不到 main 方法, 请将 main 方法定义为: public
	 * static void main(String[] args) 否则 JavaFX
	 * 应用程序类必须扩展javafx.application.Application
	 * 
	 */
}
